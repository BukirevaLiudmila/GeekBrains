var a = 1, b = 1, c, d;
c = ++a; alert(c); // 2 (префиксный инкремент сразу увеличивает значение переменной a и возвращает его)
d = b++; alert(d); // 1 (постфиксный инкремент увеличивает значение переменной a, возвращая ее старое значение)
c = (2+ ++a); alert(c); // 5 (инкремент a, а затем сложение)
d = (2+ b++); alert(d); // 4 (инкремент b, а затем сложение со старым значением b)
alert(a); // 3 (всего выполнили два инкремента)
